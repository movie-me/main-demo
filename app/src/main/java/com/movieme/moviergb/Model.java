package com.movieme.moviergb;

public class Model {

    private String movieNm, openDt, nationAlt, genreAlt, typeNm, movieCd;

    public String getMovieNm() {
        return movieNm;
    }

    public void setMovieNm(String movieNm) {
        this.movieNm = movieNm;
    }

    public String getOpenDt() {
        return openDt;
    }

    public void setOpenDt(String openDt) {
        this.openDt = openDt;
    }

    public String getNationAlt() {
        return nationAlt;
    }

    public void setNationAlt(String nationAlt) {
        this.nationAlt = nationAlt;
    }

    public String getGenreAlt() {
        return genreAlt;
    }

    public void setGenreAlt(String genreAlt) {
        this.genreAlt = genreAlt;
    }

    public String getTypeNm() {
        return typeNm;
    }

    public void setTypeNm(String typeNm) {
        this.typeNm = typeNm;
    }

    public String getMovieCd() {
        return movieCd;
    }

    public void setMovieCd(String movieCd) {
        this.movieCd = movieCd;
    }
}

